									</header>
									<p>De traductions parfois imprécises du canon pali peuvent émerger certaines confusions. Par exemple le terme <em>upekkha</em> est souvent traduis par "détachement" ou "indifférence", termes fortement connotés et ambigus, alors qu'il désigne précisément l'<em>équanimité</em>&nbsp: capacité à garder un esprit calme et équilibré en toutes circonstances. Le terme <em>tanha</em> est souvent traduit par "désir", mot extrêmement vague, alors qu'il signifie <em>soif</em> et désigne trois formes d'avidité&nbsp: attachement aux plaisirs des sens, désir de ce qui n'est pas et aversion pour ce qui est. Il est donc important de se référer au sens des mots palis pour comprendre l'enseignement.</p>
